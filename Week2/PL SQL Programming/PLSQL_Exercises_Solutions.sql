-- Scenario 3: enforce deposit/withdrawal business rules
CREATE OR REPLACE TRIGGER CheckTransactionRules
BEFORE INSERT ON Transactions
FOR EACH ROW
DECLARE
  v_balance NUMBER;
BEGIN
  IF :NEW.TransactionType = 'Deposit' THEN
    IF :NEW.Amount <= 0 THEN
      RAISE_APPLICATION_ERROR(-20030, 'Deposit amount must be positive');
    END IF;
  ELSIF :NEW.TransactionType = 'Withdrawal' THEN
    IF :NEW.Amount <= 0 THEN
      RAISE_APPLICATION_ERROR(-20031, 'Withdrawal amount must be positive');
    END IF;

    SELECT Balance INTO v_balance
    FROM Accounts
    WHERE AccountID = :NEW.AccountID;

    IF v_balance < :NEW.Amount THEN
      RAISE_APPLICATION_ERROR(-20032, 'Withdrawal exceeds available balance');
    END IF;
  END IF;
END;
