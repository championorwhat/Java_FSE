CREATE TABLE Customers (
  CustomerID   NUMBER PRIMARY KEY,
  Name         VARCHAR2(100),
  DOB          DATE,
  Balance      NUMBER,
  LastModified DATE
);

CREATE TABLE Accounts (
  AccountID    NUMBER PRIMARY KEY,
  CustomerID   NUMBER,
  AccountType  VARCHAR2(20),
  Balance      NUMBER,
  LastModified DATE,
  CONSTRAINT fk_accounts_customer FOREIGN KEY (CustomerID)
    REFERENCES Customers(CustomerID)
);

CREATE TABLE Transactions (
  TransactionID   NUMBER PRIMARY KEY,
  AccountID       NUMBER,
  TransactionDate DATE,
  Amount          NUMBER,
  TransactionType VARCHAR2(10),
  CONSTRAINT fk_transactions_account FOREIGN KEY (AccountID)
    REFERENCES Accounts(AccountID)
);

CREATE TABLE Loans (
  LoanID       NUMBER PRIMARY KEY,
  CustomerID   NUMBER,
  LoanAmount   NUMBER,
  InterestRate NUMBER,
  StartDate    DATE,
  EndDate      DATE,
  CONSTRAINT fk_loans_customer FOREIGN KEY (CustomerID)
    REFERENCES Customers(CustomerID)
);

CREATE TABLE Employees (
  EmployeeID NUMBER PRIMARY KEY,
  Name       VARCHAR2(100),
  Position   VARCHAR2(50),
  Salary     NUMBER,
  Department VARCHAR2(50),
  HireDate   DATE
);

-- Optional helper table for logging errors and audits used by solutions.
CREATE TABLE ErrorLog (
  LogID      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  LogTime    DATE DEFAULT SYSDATE,
  ModuleName VARCHAR2(100),
  Message    VARCHAR2(4000)
);

CREATE TABLE AuditLog (
  AuditID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TransactionID  NUMBER,
  ActionType     VARCHAR2(50),
  ActionTime     DATE DEFAULT SYSDATE,
  Details        VARCHAR2(4000)
);

