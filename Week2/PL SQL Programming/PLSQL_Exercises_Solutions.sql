-- Scenario 1: SafeTransferFunds
CREATE OR REPLACE PROCEDURE SafeTransferFunds(
  p_from_account IN NUMBER,
  p_to_account   IN NUMBER,
  p_amount       IN NUMBER
) AS
  v_balance NUMBER;
BEGIN
  IF p_amount <= 0 THEN
    RAISE_APPLICATION_ERROR(-20001, 'Transfer amount must be positive');
  END IF;

  SELECT Balance
  INTO v_balance
  FROM Accounts
  WHERE AccountID = p_from_account
  FOR UPDATE;

  IF v_balance < p_amount THEN
    RAISE_APPLICATION_ERROR(-20002, 'Insufficient funds');
  END IF;

  UPDATE Accounts
  SET Balance = Balance - p_amount,
      LastModified = SYSDATE
  WHERE AccountID = p_from_account;

  UPDATE Accounts
  SET Balance = Balance + p_amount,
      LastModified = SYSDATE
  WHERE AccountID = p_to_account;

  INSERT INTO Transactions(TransactionID, AccountID, TransactionDate, Amount, TransactionType)
  VALUES ((SELECT NVL(MAX(TransactionID),0)+1 FROM Transactions), p_from_account, SYSDATE, p_amount, 'Withdrawal');

  INSERT INTO Transactions(TransactionID, AccountID, TransactionDate, Amount, TransactionType)
  VALUES ((SELECT NVL(MAX(TransactionID),0)+1 FROM Transactions), p_to_account, SYSDATE, p_amount, 'Deposit');

  COMMIT;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    INSERT INTO ErrorLog(ModuleName, Message)
    VALUES ('SafeTransferFunds', 'Account not found');
    ROLLBACK;
  WHEN OTHERS THEN
    INSERT INTO ErrorLog(ModuleName, Message)
    VALUES ('SafeTransferFunds', SQLERRM);
    ROLLBACK;
    RAISE;
END;
/

-- Scenario 2: UpdateSalary
CREATE OR REPLACE PROCEDURE UpdateSalary(
  p_employee_id IN NUMBER,
  p_percent     IN NUMBER
) AS
  v_exists NUMBER;
BEGIN
  SELECT COUNT(*) INTO v_exists
  FROM Employees
  WHERE EmployeeID = p_employee_id;

  IF v_exists = 0 THEN
    RAISE_APPLICATION_ERROR(-20010, 'Employee ID does not exist');
  END IF;

  UPDATE Employees
  SET Salary = Salary + (Salary * p_percent / 100)
  WHERE EmployeeID = p_employee_id;

  COMMIT;
EXCEPTION
  WHEN OTHERS THEN
    INSERT INTO ErrorLog(ModuleName, Message)
    VALUES ('UpdateSalary', SQLERRM);
    ROLLBACK;
END;
/

-- Scenario 3: AddNewCustomer
CREATE OR REPLACE PROCEDURE AddNewCustomer(
  p_customer_id IN NUMBER,
  p_name        IN VARCHAR2,
  p_dob         IN DATE,
  p_balance     IN NUMBER
) AS
BEGIN
  INSERT INTO Customers(CustomerID, Name, DOB, Balance, LastModified)
  VALUES (p_customer_id, p_name, p_dob, p_balance, SYSDATE);
