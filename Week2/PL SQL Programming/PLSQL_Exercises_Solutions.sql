-- Scenario 1: GenerateMonthlyStatements
DECLARE
  CURSOR c_stmt IS
    SELECT c.CustomerID, c.Name, t.TransactionID, t.TransactionDate, t.Amount, t.TransactionType
    FROM Customers c
    JOIN Accounts a ON a.CustomerID = c.CustomerID
    JOIN Transactions t ON t.AccountID = a.AccountID
    WHERE TRUNC(t.TransactionDate, 'MM') = TRUNC(SYSDATE, 'MM')
    ORDER BY c.CustomerID, t.TransactionDate;

  v_prev_customer NUMBER := NULL;
BEGIN
  FOR r IN c_stmt LOOP
    IF v_prev_customer IS NULL OR v_prev_customer <> r.CustomerID THEN
      DBMS_OUTPUT.PUT_LINE('Statement for Customer ' || r.CustomerID || ' - ' || r.Name);
      v_prev_customer := r.CustomerID;
    END IF;
    DBMS_OUTPUT.PUT_LINE('  Txn ' || r.TransactionID || ' | ' ||
                         TO_CHAR(r.TransactionDate, 'YYYY-MM-DD') || ' | ' ||
                         r.TransactionType || ' | ' || r.Amount);
  END LOOP;
END;
/

-- Scenario 2: ApplyAnnualFee
DECLARE
  CURSOR c_acc IS
    SELECT AccountID, Balance
    FROM Accounts
    FOR UPDATE;
BEGIN
  FOR r IN c_acc LOOP
    UPDATE Accounts
    SET Balance = Balance - 100,
        LastModified = SYSDATE
    WHERE CURRENT OF c_acc;
  END LOOP;
  COMMIT;
END;
